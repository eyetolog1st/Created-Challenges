def input_with_timeout(prompt, timeout=45):
    def input_thread(prompt, result):
        result.append(input(prompt).strip())

    result = []
    thread = threading.Thread(target=input_thread, args=(prompt, result))
    thread.daemon = True
    thread.start()
    thread.join(timeout)

    if not result:
        print("\nTime's up!")
        exit()
    return result[0]




